--A
SELECT F.NOMBRE, P.NOMBRE
FROM FABRICANTES F
LEFT JOIN PRODUCTOS P
ON F.ID = P.ID_FABRICANTE;

--B
SELECT F.NOMBRE, P.NOMBRE
FROM FABRICANTES F
LEFT JOIN PRODUCTOS P
ON F.ID = P.ID_FABRICANTE
WHERE P.NOMBRE IS NULL;

